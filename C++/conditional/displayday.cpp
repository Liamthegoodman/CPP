#include<iostream>
using namespace std;
int main(){
    int day;
    cout<<"Enter day number:";
    cin>>day;
    if (day==1){
        cout<<"Mon"<<endl;

    }else if
        (day==2){
            cout<<"Tue"<<endl;

        }
    else if
        (day == 3){
            cout<<"Wed"<<endl;
        }
    else if
        (day == 4){
            cout<<"Thu";
        }
    else if 
        (day == 5){
            cout<<"Fri";
        }
    else if
        (day == 6){
            cout<<"Sat";

        }
    else if 
        (day == 7){
            cout<<"Sun";
        }
    else{
        cout<<"Invalid input";
    }


return 0;   
}