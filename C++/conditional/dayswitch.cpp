#include<iostream>
using namespace std;
int main(){
    int day;
    cout<<"Enter day no:";
    cin>>day;
    switch(day){
        case 1:
        cout<<"Mon";
        break;
         case 2:
        cout<<"Tue";
        break;
         case 3:
        cout<<"Wed";
        break;
         case 4:
        cout<<"Thu";
        break;
    }


return 0;   
}